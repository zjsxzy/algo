template<class T>
istream &operator>>(istream &is, vector<T> &v) {
  for (T &x : v)
    is >> x;
  return is;
}

template<class T>
ostream &operator<<(ostream &os, const vector<T> &v) {
  if (!v.empty()) {
    os << v.front();
    for (int i = 1; i < v.size(); ++i)
      os << ' ' << v[i];
  }
  return os;
}

int main() {

  int n;
  string s, t;
  cin >> n >> s >> t;
  ll ans = 0;
  int cnt = 0;
  for (int i = 0; i < n; ++i) {
    if (t[i] == '1') {
      ++cnt;
      ans -= i;
    }
    if (s[i] == '1') {
      if (cnt) {
        --cnt;
        ans += i;
      } else {
        ++cnt;
        ans -= i;
      }
    }
  }
  if (cnt == 0)
    cout << ans << '\n';
  else cout << "-1\n";

  return 0;
}
