typedef long long LL;

const int maxn = 200000 + 5;
struct BinaryIndexTree {
	int N;
	int C[maxn];
	
	void init(int N) {
		this->N = N;
		memset(C, 0, sizeof(C));
	}
	
	int lowbit(int x) {
		return x & (-x);
	}
	
	void add(int x, int num) {
		for (int i = x; i <= N; i += lowbit(i))
			C[i] += num;
	}
	
	int sum(int x) {
		int ret = 0;
		for (int i = x; i > 0; i -= lowbit(i))
			ret += C[i];
		return ret;
	}
}bit;
int arr[maxn];

int main() {
	int n;
	cin >> n;
	for (int i = 1; i <= n; i++)
		cin >> arr[i];
	LL ret = 0;
	bit.init(n);
	for (int i = 1; i <= n; i++) {
		if (arr[i] == 0) {
			ret += (LL)bit.sum(i);
		} else {
			bit.add(i, 1);
		}
	}
	cout << ret << endl;
	return 0;
}

